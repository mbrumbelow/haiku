#ifndef FAT_VM_EXTERN_H
#define FAT_VM_EXTERN_H


// Modified to support the Haiku FAT driver.

static inline void
vnode_pager_setsize(struct vnode* vp, vm_ooffset_t nsize)
{
	// This would be a good place to call file_cache_set_size, but typically the
	// node is locked at this point, and that might cause a deadlock.

	file_map_set_size(vp->v_file_map, nsize);
}


#endif // FAT_VM_EXTERN_H
